package Object;

/**
 *
 * @author ASUS
 */
public class DataKaryawan {
    private int karyawan_id;
    private String nama_karyawan;
    private String jabatan;
    private String departemen;
    private String jenis_kelamin;
    private String no_telp;
    private String alamat;
    private int masa_kerja;
    private int rate_kerja;

    public int getKaryawan_id() {
        return karyawan_id;
    }

    public void setKaryawan_id(int karyawan_id) {
        this.karyawan_id = karyawan_id;
    }

    public String getNama_karyawan() {
        return nama_karyawan;
    }

    public void setNama_karyawan(String nama_karyawan) {
        this.nama_karyawan = nama_karyawan;
    }

    public String getJabatan() {
        return jabatan;
    }

    public void setJabatan(String jabatan) {
        this.jabatan = jabatan;
    }

    public String getDepartemen() {
        return departemen;
    }

    public void setDepartemen(String departemen) {
        this.departemen = departemen;
    }

    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public int getMasa_kerja() {
        return masa_kerja;
    }

    public void setMasa_kerja(int masa_kerja) {
        this.masa_kerja = masa_kerja;
    }

    public int getRate_kerja() {
        return rate_kerja;
    }

    public void setRate_kerja(int rate_kerja) {
        this.rate_kerja = rate_kerja;
    }
    
    
}
